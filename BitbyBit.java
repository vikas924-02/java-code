class BitbyBit{
    public static void main(String[] args) {
        int a = 9 , b = 10;
        System.out.println(a/b); System.out.println(a<<2);
        System.out.println(a&b); System.out.println(b>>1);
        System.out.println(a^b); System.out.println(b>>2);
        System.out.println(a<<1);
    }
}  